// components/Payment/PaymentTablePage.tsx
import React, { useState } from 'react';
import { DollarSign, Plus, Download, AlertCircle } from 'lucide-react';
import { usePaymentData } from './hooks/usePaymentData';
import PaymentFormDialog from './Dialogs/PaymentFormDialog';
import { usePaymentActions } from './hooks/usePaymentActions';
import PaymentStats from './components/PaymentStats';
import PaymentFilters from './components/PaymentFilters';
import PaymentBulkActions from './components/PaymentBulkActions';
import PaymentTableView from './components/PaymentTableView';
import PaymentGridView from './components/PaymentGridView';
import PaymentPagination from './components/PaymentPagination';
import PaymentViewDialog from './Dialogs/PaymentViewDialog';

const PaymentTablePage: React.FC = () => {
    const [showAdvancedFilters, setShowAdvancedFilters] = useState(false);
    
    const {
        payments,
        stats,
        summary,
        loading,
        refreshing,
        error,
        currentPage,
        totalPages,
        totalItems,
        searchQuery,
        setSearchQuery,
        statusFilter,
        setStatusFilter,
        workerFilter,
        setWorkerFilter,
        dateFrom,
        setDateFrom,
        dateTo,
        setDateTo,
        paymentMethodFilter,
        setPaymentMethodFilter,
        viewMode,
        setViewMode,
        selectedPayments,
        setSelectedPayments,
        fetchPayments,
        handleRefresh,
        setCurrentPage,
        sortBy,
        setSortBy,
        sortOrder,
        setSortOrder
    } = usePaymentData();

    const [isFormDialogOpen, setIsFormDialogOpen] = useState(false);
    const [isViewDialogOpen, setIsViewDialogOpen] = useState(false);
    const [selectedPaymentId, setSelectedPaymentId] = useState<number | null>(null);
    const [dialogMode, setDialogMode] = useState<'add' | 'edit'>('add');

    const {
        handleDeletePayment,
        handleBulkDelete,
        handleExportCSV,
        handleUpdateStatus,
        handleProcessPayment,
        handleCancelPayment,
        handleGeneratePaymentSlip
    } = usePaymentActions(
        payments,
        fetchPayments,
        selectedPayments,
        setSelectedPayments
    );

    // Dialog handlers
    const openCreateDialog = () => {
        setDialogMode('add');
        setSelectedPaymentId(null);
        setIsFormDialogOpen(true);
    };

    const openEditDialog = (id: number) => {
        setDialogMode('edit');
        setSelectedPaymentId(id);
        setIsFormDialogOpen(true);
    };

    const openViewDialog = (id: number) => {
        setSelectedPaymentId(id);
        setIsViewDialogOpen(true);
    };

    const closeFormDialog = () => {
        setIsFormDialogOpen(false);
        setSelectedPaymentId(null);
    };

    const closeViewDialog = () => {
        setIsViewDialogOpen(false);
        setSelectedPaymentId(null);
    };

    const handleFormSuccess = async () => {
        await fetchPayments();
        closeFormDialog();
    };

    // Selection handlers
    const toggleSelectAll = () => {
        if (selectedPayments.length === payments.length) {
            setSelectedPayments([]);
        } else {
            setSelectedPayments(payments.map(p => p.id));
        }
    };

    const toggleSelectPayment = (id: number) => {
        setSelectedPayments(prev =>
            prev.includes(id)
                ? prev.filter(item => item !== id)
                : [...prev, id]
        );
    };

    const handleSort = (field: string) => {
        if (sortBy === field) {
            setSortOrder(sortOrder === 'asc' ? 'desc' : 'asc');
        } else {
            setSortBy(field);
            setSortOrder('asc');
        }
        setCurrentPage(1);
    };

    const handleStatusFilterChange = (status: string) => {
        setStatusFilter(status);
        setCurrentPage(1);
    };

    const handlePaymentMethodFilterChange = (method: string) => {
        setPaymentMethodFilter(method);
        setCurrentPage(1);
    };

    const clearFilters = () => {
        setStatusFilter('all');
        setWorkerFilter(null);
        setDateFrom('');
        setDateTo('');
        setPaymentMethodFilter('all');
        setSearchQuery('');
        setCurrentPage(1);
    };

    // Loading skeleton
    const renderLoadingSkeleton = () => {
        if (viewMode === 'table') {
            return (
                <div className="bg-white rounded-xl border border-gray-200 overflow-hidden">
                    <div className="overflow-x-auto">
                        <table className="w-full">
                            <thead>
                                <tr style={{ background: 'var(--table-header-bg)' }}>
                                    <th className="p-4 text-left">
                                        <div className="w-4 h-4 bg-gray-200 rounded animate-pulse"></div>
                                    </th>
                                    {['Date', 'Worker', 'Gross Pay', 'Net Pay', 'Deductions', 'Method', 'Status', 'Actions'].map((header) => (
                                        <th key={header} className="p-4 text-left">
                                            <div className="h-4 bg-gray-200 rounded animate-pulse w-24"></div>
                                        </th>
                                    ))}
                                </tr>
                            </thead>
                            <tbody>
                                {[...Array(5)].map((_, index) => (
                                    <tr key={index} className="border-b border-gray-200">
                                        <td className="p-4">
                                            <div className="w-4 h-4 bg-gray-100 rounded animate-pulse"></div>
                                        </td>
                                        <td className="p-4">
                                            <div className="flex items-center gap-3">
                                                <div className="w-8 h-8 bg-gray-100 rounded-lg animate-pulse"></div>
                                                <div>
                                                    <div className="h-4 bg-gray-100 rounded animate-pulse w-32 mb-1"></div>
                                                    <div className="h-3 bg-gray-100 rounded animate-pulse w-16"></div>
                                                </div>
                                            </div>
                                        </td>
                                        <td className="p-4">
                                            <div className="h-4 bg-gray-100 rounded animate-pulse w-20"></div>
                                        </td>
                                        <td className="p-4">
                                            <div className="h-4 bg-gray-100 rounded animate-pulse w-20"></div>
                                        </td>
                                        <td className="p-4">
                                            <div className="h-4 bg-gray-100 rounded animate-pulse w-24"></div>
                                        </td>
                                        <td className="p-4">
                                            <div className="h-6 bg-gray-100 rounded-full animate-pulse w-16"></div>
                                        </td>
                                        <td className="p-4">
                                            <div className="h-6 bg-gray-100 rounded-full animate-pulse w-16"></div>
                                        </td>
                                        <td className="p-4">
                                            <div className="flex gap-2">
                                                {[...Array(5)].map((_, i) => (
                                                    <div key={i} className="w-6 h-6 bg-gray-100 rounded animate-pulse"></div>
                                                ))}
                                            </div>
                                        </td>
                                    </tr>
                                ))}
                            </tbody>
                        </table>
                    </div>
                </div>
            );
        } else {
            return (
                <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
                    {[...Array(6)].map((_, index) => (
                        <div
                            key={index}
                            className="p-5 rounded-xl bg-white border border-gray-200 animate-pulse"
                        >
                            <div className="flex items-start gap-3 mb-4">
                                <div className="w-12 h-12 bg-gray-200 rounded-lg"></div>
                                <div className="flex-1">
                                    <div className="h-5 bg-gray-200 rounded w-3/4 mb-2"></div>
                                    <div className="h-4 bg-gray-100 rounded w-1/2"></div>
                                </div>
                            </div>
                            <div className="space-y-3 mb-4">
                                <div className="h-4 bg-gray-100 rounded w-full"></div>
                                <div className="h-4 bg-gray-100 rounded w-5/6"></div>
                                <div className="h-4 bg-gray-100 rounded w-4/6"></div>
                            </div>
                            <div className="flex items-center justify-between pt-4 border-t border-gray-200">
                                <div className="h-3 bg-gray-100 rounded w-12"></div>
                                <div className="flex gap-2">
                                    {[...Array(3)].map((_, i) => (
                                        <div key={i} className="w-6 h-6 bg-gray-100 rounded"></div>
                                    ))}
                                </div>
                            </div>
                        </div>
                    ))}
                </div>
            );
        }
    };

    // Error state
    if (error && !payments.length && !loading) {
        return (
            <div className="flex flex-col h-screen bg-gray-50">
                <div className="p-6 bg-white">
                    <div className="flex flex-col md:flex-row justify-between items-start md:items-center gap-4">
                        <div>
                            <h1 className="text-2xl font-bold flex items-center gap-2" style={{ color: 'var(--text-primary)' }}>
                                <DollarSign className="w-6 h-6" />
                                Payment Management
                            </h1>
                        </div>
                    </div>
                </div>
                <div className="flex-1 flex items-center justify-center p-6">
                    <div className="text-center p-8 bg-white rounded-xl border border-gray-200 max-w-md">
                        <AlertCircle className="w-16 h-16 mx-auto mb-4" style={{ color: 'var(--danger-color)' }} />
                        <p className="text-base font-semibold mb-2" style={{ color: 'var(--danger-color)' }}>
                            Error Loading Payment Data
                        </p>
                        <p className="text-sm mb-6 text-gray-600">{error}</p>
                        <button
                            onClick={handleRefresh}
                            className="px-6 py-3 rounded-lg text-sm font-medium transition-all duration-200 hover:shadow-md flex items-center mx-auto bg-blue-600 text-white hover:bg-blue-700"
                        >
                            Retry Loading
                        </button>
                    </div>
                </div>
            </div>
        );
    }

    return (
        <>
            <div className="flex flex-col h-screen bg-gray-50">
                {/* Header */}
                <div className="p-6 bg-white border-b border-gray-200">
                    <div className="flex flex-col md:flex-row justify-between items-start md:items-center gap-4">
                        <div>
                            <h1 className="text-2xl font-bold flex items-center gap-2" style={{ color: 'var(--text-primary)' }}>
                                <DollarSign className="w-6 h-6" />
                                Payment Management
                            </h1>
                            <p className="text-sm text-gray-600 mt-1">
                                Manage worker payments, track deductions, and monitor payment status
                            </p>
                        </div>

                        <div className="flex flex-wrap gap-3">
                            <button
                                onClick={handleExportCSV}
                                className="px-4 py-2 rounded-lg text-sm font-medium transition-all duration-200 hover:shadow-md flex items-center border border-gray-300 bg-white text-gray-700 hover:bg-gray-50"
                            >
                                <Download className="w-4 h-4 mr-2" />
                                Export CSV
                            </button>

                            <button
                                onClick={openCreateDialog}
                                className="px-4 py-2 rounded-lg text-sm font-medium transition-all duration-200 hover:shadow-md flex items-center bg-blue-600 text-white hover:bg-blue-700"
                            >
                                <Plus className="w-4 h-4 mr-2" />
                                New Payment
                            </button>
                        </div>
                    </div>
                </div>

                {/* Main Content Area */}
                <div className="flex-1 overflow-hidden">
                    <div className="h-full overflow-y-auto p-6">
                        {/* Stats Cards */}
                        <div className="mb-6">
                            <PaymentStats stats={stats} summary={summary} />
                        </div>

                        {/* Filters */}
                        <div className="mb-6">
                            <PaymentFilters
                                searchQuery={searchQuery}
                                setSearchQuery={setSearchQuery}
                                statusFilter={statusFilter}
                                setStatusFilter={setStatusFilter}
                                workerFilter={workerFilter}
                                setWorkerFilter={setWorkerFilter}
                                dateFrom={dateFrom}
                                setDateFrom={setDateFrom}
                                dateTo={dateTo}
                                setDateTo={setDateTo}
                                paymentMethodFilter={paymentMethodFilter}
                                setPaymentMethodFilter={setPaymentMethodFilter}
                                viewMode={viewMode}
                                setViewMode={setViewMode}
                                handleRefresh={handleRefresh}
                                refreshing={refreshing}
                                clearFilters={clearFilters}
                                handleStatusFilterChange={handleStatusFilterChange}
                                handlePaymentMethodFilterChange={handlePaymentMethodFilterChange}
                                sortBy={sortBy}
                                sortOrder={sortOrder}
                                handleSort={handleSort}
                                setCurrentPage={setCurrentPage}
                            />
                        </div>

                        {/* Bulk Actions */}
                        {selectedPayments.length > 0 && (
                            <div className="mb-6">
                                <PaymentBulkActions
                                    selectedCount={selectedPayments.length}
                                    onBulkDelete={handleBulkDelete}
                                    onClearSelection={() => setSelectedPayments([])}
                                />
                            </div>
                        )}

                        {/* Loading State */}
                        {loading && !refreshing && (
                            <div className="mb-6">
                                {renderLoadingSkeleton()}
                            </div>
                        )}

                        {/* Table or Grid View */}
                        {!loading && payments.length === 0 ? (
                            <div className="flex items-center justify-center h-64 rounded-xl border-2 border-dashed border-gray-300 bg-white">
                                <div className="text-center p-8">
                                    <DollarSign className="w-16 h-16 mx-auto mb-4 opacity-20" style={{ color: 'var(--text-secondary)' }} />
                                    <h3 className="text-lg font-semibold mb-2 text-gray-800">
                                        No Payments Found
                                    </h3>
                                    <p className="text-sm mb-6 max-w-md mx-auto text-gray-600">
                                        {searchQuery
                                            ? `No results found for "${searchQuery}". Try a different search term.`
                                            : 'No payments have been created yet. Get started by creating your first payment.'}
                                    </p>
                                    {!searchQuery && (
                                        <button
                                            onClick={openCreateDialog}
                                            className="px-6 py-3 rounded-lg text-sm font-medium transition-all duration-200 hover:shadow-md inline-flex items-center bg-blue-600 text-white hover:bg-blue-700"
                                        >
                                            <Plus className="w-4 h-4 mr-2" />
                                            Create First Payment
                                        </button>
                                    )}
                                </div>
                            </div>
                        ) : !loading && payments.length > 0 ? (
                            <>
                                <div className="bg-white rounded-xl border border-gray-200 overflow-hidden mb-6">
                                    {viewMode === 'table' ? (
                                        <PaymentTableView
                                            payments={payments}
                                            selectedPayments={selectedPayments}
                                            toggleSelectAll={toggleSelectAll}
                                            toggleSelectPayment={toggleSelectPayment}
                                            onView={openViewDialog}
                                            onEdit={openEditDialog}
                                            onDelete={handleDeletePayment}
                                            onUpdateStatus={handleUpdateStatus}
                                            onProcessPayment={handleProcessPayment}
                                            onCancelPayment={handleCancelPayment}
                                            onGenerateSlip={handleGeneratePaymentSlip}
                                            sortBy={sortBy}
                                            sortOrder={sortOrder}
                                            onSort={handleSort}
                                        />
                                    ) : (
                                        <div className="p-6">
                                            <PaymentGridView
                                                payments={payments}
                                                selectedPayments={selectedPayments}
                                                toggleSelectPayment={toggleSelectPayment}
                                                onView={openViewDialog}
                                                onEdit={openEditDialog}
                                                onDelete={handleDeletePayment}
                                                onUpdateStatus={handleUpdateStatus}
                                                onProcessPayment={handleProcessPayment}
                                                onGenerateSlip={handleGeneratePaymentSlip}
                                            />
                                        </div>
                                    )}
                                </div>

                                {/* Pagination */}
                                {totalPages > 1 && (
                                    <div className="bg-white rounded-xl border border-gray-200 p-4">
                                        <PaymentPagination
                                            currentPage={currentPage}
                                            totalPages={totalPages}
                                            totalItems={totalItems}
                                            limit={20}
                                            onPageChange={setCurrentPage}
                                        />
                                    </div>
                                )}
                            </>
                        ) : null}
                    </div>
                </div>
            </div>

            {/* Payment Form Dialog */}
            {isFormDialogOpen && (
                <PaymentFormDialog
                    id={selectedPaymentId || undefined}
                    mode={dialogMode}
                    onClose={closeFormDialog}
                    onSuccess={handleFormSuccess}
                />
            )}

            {/* Payment View Dialog */}
            {isViewDialogOpen && selectedPaymentId && (
                <PaymentViewDialog
                    paymentId={selectedPaymentId}
                    onClose={closeViewDialog}
                    onEdit={openEditDialog}
                />
            )}
        </>
    );
};

export default PaymentTablePage;